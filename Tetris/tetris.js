document.onkeyup = keyUpEventHandler;
function keyUpEventHandler(e) {
    if(e.keyCode === KEY.DOWN) moveSlow();
}

document.onkeydown = keyDownEventHandler;
function keyDownEventHandler(e) {
    switch(e.keyCode) {
        case KEY.LEFT: setTimeOut("moveLR(-1)", 0); break;
        case KEY.RIGHT: setTimeOut("moveLR(1)", 0); break;
        case KEY.SPACE: setTimeOut("rotateShape()", 0); break;
        case KEY.DOWN: moveFast(); break;
        case KEY.P: pause(); break;
    }
}

function init() {
    drawField();
}

